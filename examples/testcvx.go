func calcDims(dims *cvx.DimensionSet) (int, int, int) {
	cdim := dims.Sum("l", "q") + dims.SumSquared("s")
	cdim_pckd := dims.Sum("l", "q") + dims.SumPacked("s")
	cdim_diag := dims.Sum("l", "q", "s")
	return cdim, cdim_pckd, cdim_diag
}


